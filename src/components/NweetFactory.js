import React, { useState } from "react";
import { v4 as uuidv4 } from "uuid";
import { storageService, dbService } from "fbase";
import { FontAwesomeIcon } from "@fortawesome/react-fontawesome";
import { faPlus, faTimes } from "@fortawesome/free-solid-svg-icons";
import { faPaperPlane } from "@fortawesome/free-solid-svg-icons";
const NweetFactory = ({ userObj }) => {
  console.log(userObj);
  const [nweet, setNweet] = useState("");
  const [attachment, setAttachment] = useState("");
  const onSubmit = async (event) => {
    if (nweet === "") {
      return;
    }

    event.preventDefault();
    let attachmentUrl = "";
    if (attachment !== "") {
      const attachmentRef = storageService
        .ref()
        .child(`${userObj.uid}/${uuidv4()}`);
      const response = await attachmentRef.putString(attachment, "data_url");
      attachmentUrl = await response.ref.getDownloadURL();
    }
    const nweetObj = {
      text: nweet,
      createdAt: Date.now(),
      creatorId: userObj.uid,
      attachmentUrl,
      photoURL: userObj.photoURL,
    };
    await dbService.collection("nweets").add(nweetObj);
    setNweet("");
    setAttachment("");
  };
  const onChange = (event) => {
    const {
      target: { value },
    } = event;
    setNweet(value);
  };
  const onFileChange = (event) => {
    const {
      target: { files },
    } = event;
    const theFile = files[0];
    const reader = new FileReader();
    reader.onloadend = (finishedEvent) => {
      const {
        currentTarget: { result },
      } = finishedEvent;
      setAttachment(result);
    };
    reader.readAsDataURL(theFile);
  };
  const onClearAttachment = () => setAttachment("");
  return (
    <>
      <form className="factoryForm" onSubmit={onSubmit}>
        <span className="factoryFormName">
          {userObj.displayName}님 오늘 하루는 어떠셨나요?
        </span>
        <div className="factoryInput__container">
          <input //nweet입력칸
            className="factoryInput__input"
            value={nweet}
            onChange={onChange}
            type="text"
            placeholder="트윗을 작성해보세요"
            maxLength={120}
          />
          <input className="factoryInput__arrow" type="submit" value="확인" />
        </div>
        <label for="attach-file" className="factoryInput__label">
          <span>Add photos</span>
          <FontAwesomeIcon icon={faPlus} />
        </label>
        <input //+버튼을 누르면 나오는 사진 첨부컨텐츠
          id="attach-file"
          type="file"
          accept="image/*"
          ㅈ
          onChange={onFileChange}
          style={{ opacity: 0 }}
        />

        {attachment && ( //미리 보기 사진
          <div className="factoryForm__attachment">
            <img src={attachment} style={{ backgroundImage: attachment }} />

            <div className="factoryForm__clear" onClick={onClearAttachment}>
              <span>취소</span>
              <FontAwesomeIcon icon={faTimes} />
            </div>
          </div>
        )}
      </form>
    </>
  );
};
export default NweetFactory;
